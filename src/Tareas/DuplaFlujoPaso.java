public class DuplaFlujoPaso {
	private String nombre;
	private int pasos;
	public DuplaFlujoPaso(String nombre, int pasos) {
		this.nombre = nombre;
		this.pasos = pasos;
	}
	public String getNombre() {
		return nombre;
	}
	public int getPasos() {
		return pasos;
	}
	
	
}


